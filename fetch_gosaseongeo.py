GOSASEONGEO_LIST = list(dict.fromkeys(GOSASEONGEO_LIST))


def search_word(word):
    params = {
        "key": API_KEY,
        "q": word,
        "req_type": "json",
        "num": "10",
    }
    resp = requests.get(SEARCH_URL, params=params, timeout=10)
    resp.raise_for_status()
    data = resp.json()

    items = data.get("channel", {}).get("item", [])
    if isinstance(items, dict):
        items = [items]

    for item in items:
        if item.get("word") == word:
            sense = item.get("sense", {})
            if isinstance(sense, list):
                sense = sense[0]
            meaning = sense.get("definition", "").strip()
            hanja = item.get("origin", "").strip()
            if meaning:
                return {
                    "word": word,
                    "hanja": hanja,
                    "meaning": meaning,
                }
    return None


def main():
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)

    existing = {}
    if OUT_PATH.exists():
        with open(OUT_PATH, encoding="utf-8") as f:
            for entry in json.load(f):
                existing[entry["word"]] = entry
        print(f"기존 데이터 {len(existing)}개 로드")

    results = list(existing.values())
    done_words = set(existing.keys())

    total = len(GOSASEONGEO_LIST)
    for i, word in enumerate(GOSASEONGEO_LIST):
        if word in done_words:
            continue

        print(f"[{i+1}/{total}] {word}...", end=" ", flush=True)
        try:
            entry = search_word(word)
            if not entry:
                print("결과 없음")
            else:
                results.append(entry)
                print(f"OK")

            if len(results) % 20 == 0:
                with open(OUT_PATH, "w", encoding="utf-8") as f:
                    json.dump(results, f, ensure_ascii=False, indent=2)

            time.sleep(0.2)

        except Exception as e:
            print(f"오류: {e}")
            time.sleep(1)

    with open(OUT_PATH, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    print(f"\n완료: {len(results)}개 / 저장: {OUT_PATH}")


if __name__ == "__main__":
    main()
